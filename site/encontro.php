	<h2>Sobre o evento</h2>
	<p>O Clima Escolar pode ser definido como os “...sentimentos e atitudes que são trazidos à tona pelo ambiente escolar” (Loukas, 2007). Sabemos que a percepção de cada ente escolar sobre o ambiente escolar tem grande impacto em como cada qual constitui sua relação com a escola. Já há um certo consenso sobre os fatores que o impactam o clima, e diferentes abordagens e modelos existem para mensurá-lo. Porém ainda carecemos de modelos e processos práticos que podem ser usados para a transformação do clima da escola com base nesses dados.</p>
	<p>A melhora do clima está intimamente ligada ao aprimoramento das práticas democráticas na escola. Nesse movimento podemos incluir projetos de gestão participativa, mudanças nas práticas didáticas docentes e novas formas de participação dos alunos. A participação dos sujeitos nos processos de gestão escolar podem contribuir diretamente para a mudança de postura por parte dos estudantes, bem como para diminuições significativas nos problemas disciplinares dentro da escola, e consequentemente, na forma como lidam com os processos educativos.</p>
	<p>As assembleias estudantis e a justiça restaurativa para mediação e resolução de conflitos tem uma longa e rica trajetória. Ambas podem contribuir de maneira incisiva para a melhoria do clima escolar.</p>
	<p>Nesse encontro visamos reunir as experiências vividas em três escolas situadas em Campinas, São Paulo (EE Francisco Álvares, EE Benedito Sampaio e EMEF Maria Pavanatti Favaro) em torno dos pilares elencados: (1) clima escolar, (2) justiça restaurativa e (3) assembléias estudantis. Queremos ir além de uma discussão teórica sobre os méritos dos temas e de condições ideais de implementação. Visamos elencar os desafios, sucessos e questões que emergem no conflito entre o projeto proposto e a efetiva implementação. Ao final do encontro poderemos identificar pontos de encontro entre os três temas, possibilidades de troca de práticas e apoio mútuo entre as escolas participantes.</p>

	<p>Dada a natureza do encontro, este é um evento <b>somente para convidados</b>. O grupo será constituído prioritariamente de docentes, gestores e alunos das três escolas participantes. Teremos alguns convidados externos e membros das diretorias de ensino da região de Campinas.</p>


	<h2>Agenda</h2>

	<p>O encontro será organizado em torno de palestras de abertura de convidados externos sobre os temas de interesse. Seguiremos com apresentações de representantes das escolas sobre suas experiências, e de uma dinâmica de trabalho conjunto, entre docentes das escolas, para discussão dos principais desafios e sucessos na implementação dos projetos. Ao final das discussões, visamos ter um panorama desses desafios e trajetórias iniciais para implementação dos mesmos nas outras unidades escolares. As palestras serão gravadas e disponibilizadas no site Câmera Web da Unicamp. <b>Certificados</b> serão oferecidos aos participantes.</p>
